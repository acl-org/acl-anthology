"""Match author affiliations against ROR and cache coordinates + sector.

Reads affiliation strings from the Anthology's ``<author>`` tags and matches them
against the Research Organization Registry (ROR, https://ror.org). ROR is a free,
CC0 registry that provides organization identities, name variants, types, and
localities. Matching runs OFFLINE against a downloaded data dump. Where ROR links
an organization to Wikidata, the linked entity's coordinate-location statement
is fetched in batches to replace ROR's city centroid with an institution point.
Both the ROR dump and resolved coordinates are cached, so subsequent runs only
query previously unseen Wikidata IDs.

Results are written to ``data/geo/affiliation_geocache.json``. The website build
(``create_hugo_data.py``) only ever READS that file. Manual corrections live in
``data/geo/affiliation_overrides.json`` and take precedence. Re-run after new
ingestions to extend coverage:

    bin/geocode_affiliations.py --min-count 5

Unmatched affiliations are cached as ``null`` so they are not retried needlessly.
"""

import argparse
import json
import re
import sys
import unicodedata
import zipfile
from collections import Counter
from pathlib import Path
from urllib.parse import urlencode
from urllib.request import Request, urlopen

from acl_anthology import Anthology

REPO = Path(__file__).resolve().parent.parent
CACHE_PATH = REPO / "data" / "geo" / "affiliation_geocache.json"
ROR_CACHE_DIR = Path.home() / ".cache" / "acl-anthology" / "ror"
# Zenodo "concept" record that always resolves to the latest ROR data dump.
ZENODO_CONCEPT_URL = "https://zenodo.org/api/records/6347574"
WIKIDATA_API_URL = "https://www.wikidata.org/w/api.php"
USER_AGENT = (
    "acl-anthology-affiliation-map/2.0 (+https://github.com/acl-org/acl-anthology)"
)

# Written verbatim as the "_comment" key of the generated cache so the file is
# self-documenting. Keys beginning with "_" are documentation, not data, and are
# ignored by every reader (here and in create_hugo_data.py).
CACHE_COMMENT = (
    "AUTO-GENERATED by bin/geocode_affiliations.py \u2014 do not hand-edit. "
    "Affiliation strings (whitespace-normalized) are matched OFFLINE against the "
    "ROR data dump (https://ror.org) to obtain organization identity, sector, and "
    "country. Coordinates use ROR-linked Wikidata institution points where "
    "available, with ROR GeoNames locality centroids as explicit fallbacks; null "
    "means a cached match miss. Regenerate with bin/geocode_affiliations.py. "
    "Manual corrections go in affiliation_overrides.json, which overrides this file."
)

# ROR organization types (an org may have several) mapped, in priority order, to
# the coarse sectors we colour on the map.
SECTOR_BY_TYPE = [
    ("education", "academic"),
    ("company", "industry"),
    ("government", "government"),
]

# Affiliation strings that are never geocodable; skip them.
JUNK = {
    "na",
    "n/a",
    "none",
    "null",
    "-",
    "--",
    "unaffiliated",
    "independent",
    "independent researcher",
    "independent scholar",
    "independent researcher.",
    "freelance",
    "retired",
}


def normalize(raw: str) -> str:
    """Collapse whitespace. This is the cache key shared with the site build."""
    return " ".join(raw.split())


def match_key(value: str) -> str:
    """Aggressively normalize a name for matching (accents, case, punctuation)."""
    value = unicodedata.normalize("NFKD", value)
    value = "".join(c for c in value if not unicodedata.combining(c))
    return re.sub(r"[^0-9a-zA-Z]+", " ", value).strip().lower()


def is_junk(norm: str) -> bool:
    low = norm.casefold()
    return len(norm) < 3 or low in JUNK or norm.replace(".", "").isdigit()


def sector_for(types) -> str:
    for ror_type, sector in SECTOR_BY_TYPE:
        if ror_type in types:
            return sector
    return "other"


def preferred_external_id(record: dict, id_type: str) -> str | None:
    """Return ROR's preferred external ID, or its sole ID when unambiguous."""
    for external_id in record.get("external_ids", []):
        if external_id.get("type") != id_type:
            continue
        if external_id.get("preferred"):
            return external_id["preferred"]
        identifiers = external_id.get("all") or []
        if len(identifiers) == 1:
            return identifiers[0]
    return None


def coordinate_from_wikidata_entity(entity: dict) -> tuple[float, float] | None:
    """Return the best unambiguous institution-scale Wikidata coordinate."""
    coordinates = []
    for claim in entity.get("claims", {}).get("P625", []):
        if claim.get("rank") == "deprecated":
            continue
        value = claim.get("mainsnak", {}).get("datavalue", {}).get("value")
        if not value or value.get("globe") != "http://www.wikidata.org/entity/Q2":
            continue
        precision = value.get("precision")
        if precision is not None and float(precision) > 0.01:
            continue
        coordinate = (
            round(float(value["latitude"]), 5),
            round(float(value["longitude"]), 5),
        )
        coordinates.append((claim.get("rank") == "preferred", coordinate))
    if not coordinates:
        return None
    preferred = {
        coordinate for is_preferred, coordinate in coordinates if is_preferred
    }
    candidates = preferred or {coordinate for _, coordinate in coordinates}
    return candidates.pop() if len(candidates) == 1 else None


def fetch_wikidata_coordinates(
    wikidata_ids: set[str],
) -> dict[str, tuple[float, float] | None]:
    """Fetch P625 claims from Wikidata in API-sized batches."""
    identifiers = sorted(wikidata_ids)
    if not identifiers:
        return {}
    print(
        f"Resolving institution coordinates for {len(identifiers)} Wikidata IDs...",
        file=sys.stderr,
    )
    coordinates = {}
    for start in range(0, len(identifiers), 50):
        batch = identifiers[start : start + 50]
        url = WIKIDATA_API_URL + "?" + urlencode(
            {
                "action": "wbgetentities",
                "ids": "|".join(batch),
                "props": "claims",
                "format": "json",
            }
        )
        with urlopen(Request(url, headers={"User-Agent": USER_AGENT})) as response:
            entities = json.load(response).get("entities", {})
        for wikidata_id in batch:
            coordinates[wikidata_id] = coordinate_from_wikidata_entity(
                entities.get(wikidata_id, {})
            )
    return coordinates


def cached_wikidata_coordinates(
    cache: dict,
) -> dict[str, tuple[float, float] | None]:
    """Recover prior Wikidata hits and misses from affiliation cache entries."""
    coordinates = {}
    for entry in cache.values():
        if not isinstance(entry, dict) or not entry.get("wikidata_id"):
            continue
        wikidata_id = entry["wikidata_id"]
        if entry.get("coordinate_source") == "wikidata":
            coordinates[wikidata_id] = (entry["lat"], entry["lon"])
        elif wikidata_id not in coordinates:
            coordinates[wikidata_id] = None
    return coordinates


def apply_ror_metadata(cache: dict, organizations: dict) -> None:
    """Refresh ROR metadata while preserving each affiliation's matched name."""
    for entry in cache.values():
        if not isinstance(entry, dict):
            continue
        organization = organizations.get(entry.get("ror_id"))
        if not organization:
            continue
        matched_name = entry.get("name")
        entry.pop("wikidata_id", None)
        entry.update(organization)
        if matched_name is not None:
            entry["name"] = matched_name


def apply_wikidata_coordinates(cache: dict, coordinates: dict) -> Counter:
    """Replace locality fallbacks with available Wikidata institution points."""
    sources_by_organization = {}
    for entry in cache.values():
        if not isinstance(entry, dict):
            continue
        wikidata_id = entry.get("wikidata_id")
        coordinate = coordinates.get(wikidata_id)
        if coordinate:
            entry["lat"], entry["lon"] = coordinate
            entry["coordinate_source"] = "wikidata"
        if entry.get("ror_id"):
            sources_by_organization[entry["ror_id"]] = entry.get(
                "coordinate_source", "unknown"
            )
    return Counter(sources_by_organization.values())


def resolve_ror_dump(refresh: bool = False) -> Path:
    """Return a path to the ROR data dump JSON, downloading it if necessary."""
    ROR_CACHE_DIR.mkdir(parents=True, exist_ok=True)
    existing = sorted(ROR_CACHE_DIR.glob("*ror-data*.json"))
    if existing and not refresh:
        return existing[-1]

    print("Resolving latest ROR data dump from Zenodo...", file=sys.stderr)
    with urlopen(Request(ZENODO_CONCEPT_URL, headers={"User-Agent": USER_AGENT})) as r:
        meta = json.load(r)
    zip_file = next(f for f in meta["files"] if f["key"].endswith(".zip"))
    url = zip_file["links"]["self"]
    zip_path = ROR_CACHE_DIR / zip_file["key"]
    print(
        f"Downloading {zip_file['key']} ({zip_file['size'] / 1e6:.0f} MB)...",
        file=sys.stderr,
    )
    with urlopen(Request(url, headers={"User-Agent": USER_AGENT})) as response:
        zip_path.write_bytes(response.read())
    with zipfile.ZipFile(zip_path) as archive:
        json_names = [n for n in archive.namelist() if n.endswith(".json")]
        # Prefer the v2-schema file when a dump ships both v1 and v2.
        target = next((n for n in json_names if "schema_v2" in n), None) or next(
            (n for n in json_names if "v2" in n), json_names[0]
        )
        archive.extract(target, ROR_CACHE_DIR)
    zip_path.unlink()
    return ROR_CACHE_DIR / target


def build_ror_index(dump_path: Path):
    """Build {normalized name -> org info} indexes from the ROR dump.

    Full names (display/label/alias) and acronyms are indexed separately so that
    full names win. Names shared by more than one organization are dropped, to
    avoid placing an affiliation at the wrong institution.
    """
    print(f"Loading ROR dump: {dump_path.name}", file=sys.stderr)
    records = json.loads(dump_path.read_text(encoding="utf-8"))
    full_index: dict = {}
    acronym_index: dict = {}
    organizations: dict = {}
    for record in records:
        locations = record.get("locations") or []
        geo = locations[0].get("geonames_details") if locations else None
        if not geo or "lat" not in geo or "lng" not in geo:
            continue
        info = {
            "lat": round(float(geo["lat"]), 5),
            "lon": round(float(geo["lng"]), 5),
            "sector": sector_for(record.get("types", [])),
            "country": geo.get("country_code"),
            "ror_id": record.get("id"),
            "source": "ror",
            "coordinate_source": "ror-geonames",
        }
        wikidata_id = preferred_external_id(record, "wikidata")
        if wikidata_id:
            info["wikidata_id"] = wikidata_id
        organizations[info["ror_id"]] = info
        for name in record["names"]:
            key = match_key(name["value"])
            if len(key) < 3:
                continue
            index = acronym_index if "acronym" in name["types"] else full_index
            if key in index and index[key] and index[key]["ror_id"] != info["ror_id"]:
                index[key] = None  # ambiguous name -> do not match it
            elif key not in index:
                index[key] = dict(info, name=name["value"])
    print(
        f"Indexed {sum(1 for v in full_index.values() if v)} unambiguous full names.",
        file=sys.stderr,
    )
    return full_index, acronym_index, organizations


def match_affiliation(raw: str, full_index: dict, acronym_index: dict):
    """Return org info for an affiliation string, or None if no confident match."""
    whole = match_key(raw)
    if full_index.get(whole):
        return full_index[whole]
    if acronym_index.get(whole):
        return acronym_index[whole]
    # Try comma / semicolon / slash separated segments (e.g. "Dept X, Univ Y").
    for segment in re.split(r"[;,/]", raw):
        key = match_key(segment)
        if len(key) >= 5 and full_index.get(key):
            return full_index[key]
    return None


def collect_affiliations() -> Counter:
    """Count author-slot occurrences of each normalized affiliation string."""
    anthology = Anthology.from_within_repo()
    counts: Counter = Counter()
    for paper in anthology.papers():
        for namespec in paper.authors:
            if namespec.affiliation:
                norm = normalize(namespec.affiliation)
                if norm:
                    counts[norm] += 1
    return counts


def save_cache(cache: dict) -> None:
    # Lead with a provenance note (keys starting with "_" are documentation, not
    # data). Entries and their fields are sorted for stable, review-friendly diffs.
    payload = {"_comment": CACHE_COMMENT}
    for key in sorted(cache):
        entry = cache[key]
        payload[key] = dict(sorted(entry.items())) if isinstance(entry, dict) else entry
    CACHE_PATH.write_text(
        json.dumps(payload, ensure_ascii=False, indent=1) + "\n",
        encoding="utf-8",
    )


def main() -> None:
    parser = argparse.ArgumentParser(
        description="Match author affiliations against ROR and cache the results."
    )
    parser.add_argument(
        "--min-count",
        type=int,
        default=5,
        help="Only match affiliations appearing at least this many times.",
    )
    parser.add_argument(
        "--limit",
        type=int,
        default=None,
        help="Optionally cap the number of NEW affiliations matched this run.",
    )
    parser.add_argument(
        "--ror-file",
        type=Path,
        default=None,
        help="Path to a ROR data dump JSON (default: download/cache the latest).",
    )
    parser.add_argument(
        "--refresh",
        action="store_true",
        help="Force a re-download of the ROR data dump.",
    )
    parser.add_argument(
        "--refresh-wikidata",
        action="store_true",
        help="Re-query all linked Wikidata coordinates, including cached misses.",
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Report what would be matched without loading ROR or writing.",
    )
    args = parser.parse_args()

    cache: dict = {}
    if CACHE_PATH.exists():
        raw = json.loads(CACHE_PATH.read_text(encoding="utf-8"))
        # Drop the "_comment" documentation key (see CACHE_COMMENT).
        cache = {k: v for k, v in raw.items() if not k.startswith("_")}
    print(f"Loaded {len(cache)} cached affiliations.", file=sys.stderr)
    needs_coordinate_migration = any(
        isinstance(entry, dict)
        and entry.get("ror_id")
        and not entry.get("coordinate_source")
        for entry in cache.values()
    )

    counts = collect_affiliations()
    print(f"Found {len(counts)} distinct affiliation strings.", file=sys.stderr)

    todo = [
        norm
        for norm, count in counts.most_common()
        if norm not in cache and count >= args.min_count and not is_junk(norm)
    ]
    if args.limit is not None:
        todo = todo[: args.limit]
    print(f"{len(todo)} affiliations to match against ROR.", file=sys.stderr)

    if needs_coordinate_migration:
        print(
            "Existing ROR entries need institution-coordinate migration.",
            file=sys.stderr,
        )
    if args.dry_run:
        for norm in todo:
            print(f"  {counts[norm]:6d}  {norm}")
        return
    if not todo and not needs_coordinate_migration and not args.refresh_wikidata:
        return

    dump = args.ror_file or resolve_ror_dump(refresh=args.refresh)
    full_index, acronym_index, organizations = build_ror_index(dump)
    wikidata_coordinates = (
        {} if args.refresh_wikidata else cached_wikidata_coordinates(cache)
    )

    sectors: Counter = Counter()
    matched = 0
    for norm in todo:
        info = match_affiliation(norm, full_index, acronym_index)
        cache[norm] = info
        if info:
            matched += 1
            sectors[info["sector"]] += 1
    apply_ror_metadata(cache, organizations)
    linked_wikidata_ids = {
        entry["wikidata_id"]
        for entry in cache.values()
        if isinstance(entry, dict) and entry.get("wikidata_id")
    }
    wikidata_coordinates.update(
        fetch_wikidata_coordinates(
            linked_wikidata_ids - wikidata_coordinates.keys()
        )
    )
    coordinate_sources = apply_wikidata_coordinates(cache, wikidata_coordinates)
    save_cache(cache)

    print(
        f"Matched {matched}/{len(todo)} ({100 * matched / max(len(todo), 1):.0f}%). "
        f"By sector: {dict(sectors)}. Coordinates by organization: "
        f"{dict(coordinate_sources)}. Cache now holds {len(cache)} entries.",
        file=sys.stderr,
    )


if __name__ == "__main__":
    main()
